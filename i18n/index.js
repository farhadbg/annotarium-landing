      var labels = { en: 'EN', es: 'ES', fr: 'FR', zh: 'ZH', ja: 'JA' };

      function setLang(lang) {
        if (!T[lang]) return;
        try { localStorage.setItem('app-language', lang); } catch(e) {}
        document.documentElement.lang = lang;

        // Update text content
        document.querySelectorAll('[data-i18n]').forEach(function(el) {
          var key = el.getAttribute('data-i18n');
          if (T[lang][key] != null) el.textContent = T[lang][key];
        });
        // Update innerHTML for elements that contain markup
        document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
          var key = el.getAttribute('data-i18n-html');
          if (T[lang][key] != null) el.innerHTML = T[lang][key];
        });

        // Update label
        var lbl = document.getElementById('lang-label');
        var lblM = document.getElementById('lang-label-mobile');
        if (lbl) lbl.textContent = labels[lang];
        if (lblM) lblM.textContent = labels[lang];

        // Highlight active
        document.querySelectorAll('.lang-option').forEach(function(btn) {
          var isActive = btn.getAttribute('data-lang') === lang;
          btn.className = 'lang-option w-full text-left px-3 py-1.5 text-sm transition-colors ' +
            (isActive ? 'bg-sky-50 text-sky-700 font-medium' : 'text-slate-700 hover:bg-slate-50');
        });

        // Close menus
        document.getElementById('lang-menu').classList.add('hidden');
        document.getElementById('lang-menu-mobile').classList.add('hidden');
      }

      // Toggle menus
      function updateChevrons() {
        var chevron = document.getElementById('lang-chevron');
        var chevronM = document.getElementById('lang-chevron-mobile');
        var menuOpen = !document.getElementById('lang-menu').classList.contains('hidden');
        var menuMOpen = !document.getElementById('lang-menu-mobile').classList.contains('hidden');
        if (chevron) chevron.style.transform = menuOpen ? 'rotate(180deg)' : '';
        if (chevronM) chevronM.style.transform = menuMOpen ? 'rotate(180deg)' : '';
      }

      function toggleMenu(menuId) {
        var menu = document.getElementById(menuId);
        var other = menuId === 'lang-menu' ? 'lang-menu-mobile' : 'lang-menu';
        document.getElementById(other).classList.add('hidden');
        menu.classList.toggle('hidden');
        updateChevrons();
      }

